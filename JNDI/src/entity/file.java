package entity;

public class file {
	private int fid;
	private String file;
	
	
	public file(String file) {
		super();
		this.file = file;
	}
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	
	
}
